package sec_verify03;

public class SmartPhone<C, M, I> {
	private C company;
	private M model;
	private I inch;

	public C getCompany() {
		return company;
	}

	public void setCompany(C company) {
		this.company = company;
	}

	public M getModel() {
		return model;
	}

	public void setModel(M model) {
		this.model = model;
	}

	public I getInch() {
		return inch;
	}

	public void setInch(I inch) {
		this.inch = inch;
	}

}
